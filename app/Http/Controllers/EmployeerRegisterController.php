     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'g-recaptcha-response' => 'required|captcha',
            'email'=>'required|email',
            'phone'=>'required|min:7|',
            'password'=>'required|min:8|confirmed'
        ]);

                $empReg = new Employeer;
                $empReg->email = $request['email'];
                $empReg->phone = $request['phone'];
                $empReg->password = Hash::make($request['password']);
                $empReg->save();
                return redirect()->back()->with('flash_message_success','You are registered as employer');

        }

